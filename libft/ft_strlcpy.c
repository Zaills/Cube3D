#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	int					len;
	unsigned long int	i;

	len = ft_strlen(src);
	if (size < 1)
		return (len);
	i = 0;
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (len);
}
